let firstNum = prompt('Введите первое число');
let secondNum = prompt('Введите второе число');

console.log(`${firstNum} + ${secondNum} = ${+firstNum + +secondNum}`);
console.log(`${firstNum} - ${secondNum} = ${firstNum - secondNum}`);
console.log(`${firstNum} * ${secondNum} = ${firstNum * secondNum}`);
console.log(`${firstNum} / ${secondNum} = ${firstNum / secondNum}`);